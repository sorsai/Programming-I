
public class Rhombus {
		public static void main(String[] args) {
			top();
			bottom();
			mid();
			bottom();
			top();
			mid();
			bottom();
		}	
		public static void top() {
			System.out.println("    $$$    ");
			System.out.println("   $   $   ");
			System.out.println("  $     $  ");
		}		
		public static void mid() {
			System.out.println(" \"-'-\"-'-\"");
		}
		public static void bottom() {	
			System.out.println("  $     $  ");
			System.out.println("   $   $   ");
			System.out.println("    $$$    ");
		}

	}



