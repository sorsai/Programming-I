
public class RhombusStart {
	public static void main(String[] args) {
		top();
		fin();
		fin();
		sign();
		top();
		start();
		fin();
		sign();
	}
	public static void top() {
		System.out.println("   $$$   ");
		System.out.println("  $   $  ");
		System.out.println(" $     $ ");
	}
	public static void fin() {
		System.out.println(" $     $ ");
		System.out.println("  $   $  ");
		System.out.println("   $$$   ");
	}
	public static void sign() {
		System.out.println("+--------+");
	}
	public static void start() {
		System.out.println("| START |");
	}
}

